//WAP to print sum of all the values in a given array.
//input:
//arr=[12,90,34,56,33]

public class Example7 {
public static void main(String[] args) {
int sum=0;
    //Array declaration
int ar[]={12,90,34,56,33};
for(int i=0;i<=4;i++) {
System.out.println(ar[i]);
sum=sum+ar[i];
    }
System.out.println("Total sum of the element="+sum);
  }

}
  